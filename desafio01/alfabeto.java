package desafio01;

public class alfabeto {

	public Integer getNumero(String letra) {
		Integer num = null;
		String alpha = letra.toLowerCase();
		
		if(alpha.equals("a")) num = 1;
		if(alpha.equals("b")) num = 2;
		if(alpha.equals("c")) num = 3;
		if(alpha.equals("d")) num = 4;
		if(alpha.equals("e")) num = 5;
		if(alpha.equals("f")) num = 6;
		if(alpha.equals("g")) num = 7;
		if(alpha.equals("h")) num = 8;
		if(alpha.equals("i")) num = 9;
		if(alpha.equals("j")) num = 10;
		if(alpha.equals("k")) num = 11;
		if(alpha.equals("l")) num = 12;
		if(alpha.equals("n")) num = 13;
		if(alpha.equals("m")) num = 14;
		if(alpha.equals("o")) num = 15;
		if(alpha.equals("p")) num = 16;
		if(alpha.equals("q")) num = 17;
		if(alpha.equals("r")) num = 18;
		if(alpha.equals("s")) num = 19;
		if(alpha.equals("t")) num = 20;
		if(alpha.equals("u")) num = 21;
		if(alpha.equals("v")) num = 22;
		if(alpha.equals("w")) num = 23;
		if(alpha.equals("x")) num = 24;
		if(alpha.equals("y")) num = 25;
		if(alpha.equals("z")) num = 26;
		
		return num;
	}
}
